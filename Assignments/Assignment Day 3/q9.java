class Q9{
	public static void main(String []arg){



		System.out.println("Using for loop");

		for (int i = 10;i<=20;i++){
			System.out.println(i);			
		}

		System.out.println("Using while loop");
		
		int i = 10;
		while(i<=20){
			System.out.println(i);
			i++;	
		}
		System.out.println("Using do while loop");
		
		i = 10;
		do{
			System.out.println(i);
			i++;	
		}while(i<=20);


	}	
}